public class enemy {
    int xvalue, yvalue, xSpeed, ySpeed, sizex, sizey;

    public enemy(int x, int y, int xs, int ys, int xsize, int ysize){
        this.xvalue = x;
        this.yvalue = y;
        this.xSpeed = xs;
        this.ySpeed = ys;
        this.sizex = xsize;
        this.sizey = ysize;
    }
}